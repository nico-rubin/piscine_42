#include "rush_02.h"

char	*ft_pass_middle(char *raw)
{
	while (*raw == ' ')
		raw++;
	raw++;
	while (*raw == ' ')
		raw++;
	return (raw);
}

void	ft_putstr(char *str)
{
	while (*str)
	{
		write(1, str, 1);
		str++;
	}
}
